// Get sorted countries list
export const getSortedCountries = () => {
  return Object.entries(countriesData)
    .map(([code, data]) => ({
      code,
      name: data.name,
      flag: data.flag,
      phoneCode: data.code,
      cities: data.cities
    }))
    .sort((a, b) => a.name.localeCompare(b.name, 'tr'));
};

// Get cities for a specific country
export const getCitiesForCountry = (countryCode) => {
  const country = countriesData[countryCode];
  return country ? country.cities.sort((a, b) => a.localeCompare(b, 'tr')) : [];
};